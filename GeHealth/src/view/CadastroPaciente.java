    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        String nome = txtNomeCompleto.getText().trim();
        String cpf = txtCPF.getText().trim();
        String dataNasc = txtDataNascimento.getText().trim();
        String telefone = txtTelefone.getText().trim();  // Novo campo para telefone

        // Verifica se os campos obrigat√≥rios est√£o preenchidos
        if (nome.isEmpty() || cpf.isEmpty() || dataNasc.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Os campos Nome, CPF e Data de Nascimento s√£o obrigat√≥rios.");
            return;
        }

        // Verifica se o CPF e a Data s√£o v√°lidos
        if (!validarCPF(cpf)) {
            JOptionPane.showMessageDialog(this, "CPF inv√°lido.");
            return;
        }
        String dataFormatada = validarEFormatarData(dataNasc);
        if (dataFormatada == null) {
            JOptionPane.showMessageDialog(this, "Data de nascimento inv√°lida.");
            return;
        }

        // Verifica o telefone, se preenchido
        if (!telefone.isEmpty() && !validarTelefone(telefone)) {
            JOptionPane.showMessageDialog(this, "Telefone inv√°lido. O formato correto √© (XX) XXXXX-XXXX.");
            return;
        }

        // Cria o objeto paciente e atribui os valores
        Paciente paciente = new Paciente();
        paciente.setNome(nome);
        paciente.setCpf(cpf);
        paciente.setDataNascimento(dataFormatada);
        paciente.setEmail(txtEmail.getText().trim());
        paciente.setTelefone(telefone);  // Atualiza o telefone (pode ser vazio ou v√°lido)

        // Cadastro realizado com sucesso
        if (pacientedao.conectar()) {
            if (pacientedao.salvar(paciente) == 1) { // Use a inst√¢ncia recebida
                pacientedao.desconectar();
                dispose();
            }
        }
    }//GEN-LAST:event_btnSalvarActionPerformed

